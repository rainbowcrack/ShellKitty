export TERM=xterm-256color

PALETTE=(219 218 217 212 211 207 206 201 200 199)

print_gradient() {
  local i=0 n=${#PALETTE[@]}
  while IFS= read -r line; do
    printf "\e[38;5;%sm%s\e[0m\n" "${PALETTE[i]}" "$line"
    (( i=(i+1)%n ))
  done
}

